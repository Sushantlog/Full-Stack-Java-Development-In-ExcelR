public class _03_Comparison {

    public static void main(String[] args) {
        int x =20;
        int y =20;
        // Comparison ==
        System.out.println(x == y);

        // Comparison !=
        System.out.println(x != y);

        // Grater than >
        System.out.println(x  > y);

        // Smaller than <
        System.out.println(x < y);

        // Grater than or equal to 
        System.out.println(x >= y);

        // Smaller than < equal to 
        System.out.println(x <= y);



    }
}