public class _01_Arithmetic {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;
        
        // Addition operation 
        System.out.println(x + y);

        // Substraction operator
        System.out.println(x - y);

        // Multiplication 
        System.out.println(x * y);

        // Division
        System.out.println(x / y);

        // Modulus
        System.out.println(x % y);

        // Increment / Decrement 
        System.out.println(x++);
        System.out.println("x : "+ x);
        System.out.println(y--);
        System.out.println("y : "+ y);
    }
}
