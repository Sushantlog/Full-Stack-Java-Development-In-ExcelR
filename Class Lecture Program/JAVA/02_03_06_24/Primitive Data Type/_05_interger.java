public class _05_interger {
    public static void main(String[] args) {
        // byte type
        byte  myNum = 100;
        System.out.println(myNum);

        // short type
        short myNum2 = 5000;
        System.out.println(myNum2);

        // int type
        int myNum3 = 10000;
        System.out.println(myNum3);

        int myNum4 = 1005;
        System.out.println(myNum4);
    }
} 
