class Student{
    // Attributes of a student
    int rollNo;
    String name, address;

    public Student(int rollNo,String name,String address){
        this.rollNo = rollNo;
        this.name = name;
        this.address = address;
    }
    // To print student details
    public String toString(){
        return this.rollNo + " " +this.name +" "+this.address;
    }
}


